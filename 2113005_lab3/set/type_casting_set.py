# Ép kiểu đối tượng trong Python3 thành các tập hợp (sets)

# Ép kiểu danh sách thành tập hợp
my_list = [1, 2, 3, 3, 4, 5, 5, 6, 2]
my_set = set(my_list)
print("my_list dưới dạng một tập hợp: ", my_set)

# Ép kiểu chuỗi thành tập hợp
my_str = "GeeksforGeeks"
my_set1 = set(my_str)
print("my_str dưới dạng một tập hợp: ", my_set1)

# Ép kiểu từ điển thành tập hợp
my_dict = {1: "One", 2: "Two", 3: "Three"}
my_set2 = set(my_dict)
print("my_dict dưới dạng một tập hợp: ", my_set2)


