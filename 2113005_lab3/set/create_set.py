# Chương trình Python để thể hiện
# Tạo một Tập hợp trong Python

# Tạo một Tập hợp (Set)
set1 = set()
print("Tập hợp ban đầu rỗng: ")
print(set1)

# Tạo một Tập hợp bằng cách
# sử dụng một chuỗi
set1 = set("GeeksForGeeks")
print("\nTập hợp được tạo bằng cách sử dụng chuỗi: ")
print(set1)

# Tạo một Tập hợp bằng cách
# sử dụng Constructor
# (Sử dụng đối tượng để lưu trữ chuỗi)
Chuoi = 'GeeksForGeeks'
set1 = set(Chuoi)
print("\nTập hợp được tạo bằng cách sử dụng một Đối tượng: " )
print(set1)

# Tạo một Tập hợp bằng cách
# sử dụng một Danh sách
set1 = set(["Geeks", "For", "Geeks"])
print("\nTập hợp được tạo bằng cách sử dụng một Danh sách: ")
print(set1)
